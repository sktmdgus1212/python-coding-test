# 분, 초: 3, 13, 23, 30~39, 43, 53 -> 15개
# 3시가 아닌 나머지 시간에 겹치는 수: 15 * 60 + 15 * 45 = 15 * 105 = 1575
# 시: 3, 13, 23 -> 3개
# 3시인 경우 60 * 60 = 3600

n = int(input())
result = 0
if n < 3:
    result = 1575 * (n+1)
elif n < 13:
    result = 1575 * n + 3600
elif n < 23:
    result = 1575 * (n-1) + 3600 * 2
else:
    result = 1575 * (n-2) + 3600 * 3
print(result)

